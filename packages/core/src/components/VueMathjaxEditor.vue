    currentFontSize.value = computedStyle.fontSize || '16px'
    
    // Êõ¥Êñ∞Â≠ó‰ΩìÈ¢úËâ≤
    const color = computedStyle.color
    if (color && color !== 'rgb(51, 65, 85)') {
