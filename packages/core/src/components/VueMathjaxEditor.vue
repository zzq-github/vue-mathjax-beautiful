  }

  // Êüê‰∫õÈîÆ‰ºöÈáçÁΩÆÊ†ºÂºèÁä∂ÊÄÅ
  if (['ArrowLeft', 'ArrowRight', 'ArrowUp', 'ArrowDown', 'Home', 'End'].includes(event.key)) {
