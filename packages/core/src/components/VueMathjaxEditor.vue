  htmlContent = htmlContent
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>') // Á≤ó‰Ωì
    .replace(/\*(.*?)\*/g, '<em>$1</em>') // Êñú‰Ωì
    .replace(/__(.*?)__/g, '<u>$1</u>') // ‰∏ãÂàíÁ∫ø
    .replace(/~~(.*?)~~/g, '<s>$1</s>') // Âà†Èô§Á∫ø
